package com.App.Thwaap;

public class item {
    private  String te;
    private  String tee;
    private String teee;
    private String teeee;

    public item(String te, String tee, String teee, String teeee) {
        this.te = te;
        this.tee = tee;
        this.teee = teee;
        this.teeee = teeee;
    }

    public String getTe() {
        return te;
    }

    public void setTe(String te) {
        this.te = te;
    }

    public String getTee() {
        return tee;
    }

    public void setTee(String tee) {
        this.tee = tee;
    }

    public String getTeee() {
        return teee;
    }

    public void setTeee(String teee) {
        this.teee = teee;
    }

    public String getTeeee() {
        return teeee;
    }

    public void setTeeee(String teeee) {
        this.teeee = teeee;
    }
}
